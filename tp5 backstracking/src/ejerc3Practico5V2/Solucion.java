package ejerc3Practico5V2;
import java.util.List;
import java.util.LinkedList;
public class Solucion {
	List<List<Integer>> valoresSolucion = new LinkedList<>();

	public Solucion(List<List<Integer>> valoresSolucion) {
		super();
		this.valoresSolucion = valoresSolucion;
	}

	public List<List<Integer>> getValoresSolucion() {
		return valoresSolucion;
	}

	public void setValoresSolucion(List<List<Integer>> valoresSolucion) {
		this.valoresSolucion = valoresSolucion;
	}
	
}
